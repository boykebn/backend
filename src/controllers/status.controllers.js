const { selectAllStatus, selectStatussId, insertStatus, updateStatus, deletedStatus } = require('../models/status.models')
const errorHandler = require('../helpers/errorHandler.helpers')

exports.readAllStatus = (req, res) => {
  selectAllStatus(req.body, (err, data) => {
    if(err){
      console.log(err)
      return errorHandler(err, res);
    }
    return res.status(200).json({
      succes: true,
      message: 'Database access sucsessfully',
      results: data.rows
    })
  })
};

